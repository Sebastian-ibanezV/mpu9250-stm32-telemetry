/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* MCU Configuration--------------------------------------------------------*/
  HAL_Init();
  SystemClock_Config();

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_I2C1_Init();
  MX_SPI1_Init();
  MX_TIM1_Init();
  MX_USART2_UART_Init();

  /* USER CODE BEGIN 2 */
  HAL_Delay(200);
  uart_print("Boot...\r\n");

  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_3);
  HAL_TIM_PWM_Start(&htim1, TIM_CHANNEL_4);

  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_1, 1000);
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_2, 1000);
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_3, 1000);
  __HAL_TIM_SET_COMPARE(&htim1, TIM_CHANNEL_4, 1000);

  MPU_Status st = MPU9250_Init(&hi2c1, &mpu);
  snprintf(txbuf, sizeof(txbuf),
           "MPU init st=%u who=0x%02X addr=0x%02X\r\n",
           (unsigned)st, (unsigned)mpu.whoami, (unsigned)(mpu.i2c_addr >> 1));
  uart_print(txbuf);

  if (st != MPU_OK) {
    uart_print("MPU init FAIL (check I2C wiring/pullups/addr)\r\n");
  } else {
    uart_print("MPU init OK\r\n");
  }

  uart_print("Calibrating gyro... keep it still\r\n");
  st = MPU9250_Calibrate_Gyro(&hi2c1, &mpu, 800);

  snprintf(txbuf, sizeof(txbuf),
           "Gyro calib st=%u off: %.3f %.3f %.3f\r\n",
           (unsigned)st,
           (double)mpu.Gx_offset, (double)mpu.Gy_offset, (double)mpu.Gz_offset);
  uart_print(txbuf);

  uart_print("CSV stream: t_ms,ax,ay,az,gx,gy,gz,acc_ok\r\n");
  uart_print("t_ms,ax,ay,az,gx,gy,gz,acc_ok\r\n");
  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

    /* USER CODE BEGIN 3 */
    MPU_Status r = MPU9250_Read_Accel_Gyro(&hi2c1, &mpu);

    if (r == MPU_OK) {
      // CSV line (100 Hz)
      snprintf(txbuf, sizeof(txbuf),
               "%lu,%.5f,%.5f,%.5f,%.3f,%.3f,%.3f,%u\r\n",
               (unsigned long)HAL_GetTick(),
               (double)mpu.Ax, (double)mpu.Ay, (double)mpu.Az,
               (double)mpu.Gx, (double)mpu.Gy, (double)mpu.Gz,
               (unsigned)mpu.accel_sanity_ok);
      uart_print(txbuf);
    } else {
      // Error line
      snprintf(txbuf, sizeof(txbuf), "err,%lu,%u\r\n",
               (unsigned long)HAL_GetTick(), (unsigned)r);
      uart_print(txbuf);
    }

    HAL_Delay(10); // ~100 Hz
  }
  /* USER CODE END 3 */
}

